CREATE TABLE Alumni(
Alum_id INT,
AlumInfoNameFirst VARCHAR(255),
AlumInfoNameLast VARCHAR(255),
AlumInfoEmail VARCHAR(100),
AlumAcademicsMajor CHAR(3),
AlumCareerField VARCHAR(255)
);

CREATE TABLE Students(
Student_id INT,
StudentInfoNameFirst VARCHAR(255),
StudentInfoNameLast VARCHAR(255),
StudentInfoEmail VARCHAR(100),
StudentAcademicsMajor CHAR(3),
StudentCareerDesiredField VARCHAR(255)
);
